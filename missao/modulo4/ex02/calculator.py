#!/usr/bin/env python3

n_01 = int(input("Me de o primeiro numero: "))
n_02 = int(input("Me de o segundo numero: "))
print("Obrigado!")

add = n_01 + n_02
print(f"{n_01} + {n_02} = {add}")

sub = n_01 - n_02
print(f"{n_01} - {n_02} = {sub}")

div = int((n_01 / n_02))
print(f"{n_01} / {n_02} = {div}")

mul = n_01 * n_02
print(f"{n_01} * {n_02} = {mul}")