#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int main (void)
{
  char string1[] = "Geovana";
  char string2[] = "GeOvana";

  int numero = ft_strncmp(string1, string2, 3);
  printf("%d", numero);
  return (0);
}